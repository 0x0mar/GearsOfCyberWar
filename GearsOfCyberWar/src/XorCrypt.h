/**
<summary>
<para>Simple implementation of the XOR cipher. The XOR cipher is an additive
cipher that operates according to the principles:</para>
A (+) 0 = A
A (+) A = 0
<para>This implementation uses the characters of the given byte array and
XOR each byte of the given with the corresponding byte of the given key.</para>
</summary>
<param name="CipherData">An array of bytes to XOR. Must contain at least 
one item.</param>
<param name="DataLength">The number of bytes passed in CipherData.</param>
<param name="Key">An array of bytes that act as the symmetric key to the
XOR cipher.</param>
<param name="KeyLength">The number of bytes in the key.</param>
<returns>An array of bytes XOR'd with the given key.</returns>
<seealso cref="http://en.wikipedia.org/wiki/XOR_cipher">XOR Cipher</seealso>
*/
unsigned char* XorCrypt(unsigned char* CipherData, unsigned int DataLength, char* Key, unsigned int KeyLength);